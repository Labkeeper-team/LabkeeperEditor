import { expect, test } from '@playwright/test';

/*
403 при получении проекта незалогиненным пользователем
 */
test('project-403-test', async ({ page }) => {
    let rejectedRequests = 0;

    // Блокируем все запросы по умолчанию
    await page.route('/api/*', async (route) => {
        rejectedRequests++;
        await route.abort();
    });

    // Разрешаем только необходимые запросы
    await page.route('/api/v2/public/user-info', async (route) => {
        await route.fulfill({
            status: 200,
            contentType: 'application/json',
            body: JSON.stringify({
                isAuthenticated: false,
                email: 'a@gmail.com',
                id: 1,
            }),
        });
    });

    await page.route('/api/v2/public/project/1/get', async (route) => {
        await route.fulfill({
            status: 403,
            contentType: 'application/json',
        });
    });

    // Перехватываем запрос на список файлов
    await page.route('/api/v2/public/project/1/file/list', async (route) => {
        await route.fulfill({
            status: 200,
            contentType: 'application/json',
            body: JSON.stringify({
                files: [
                    {
                        autogenerated: false,
                        fileName: 'example.txt',
                        url: '/files/example.txt',
                    },
                ],
            }),
        });
    });

    await page.goto('/project/1');

    // Ждем загрузки страницы
    await page.waitForLoadState('networkidle');

    // ждем появления toast с ошибкой
    await expect(page.locator('div.Toastify__toast').first()).toBeVisible();

    // Проверяем, что не было отклоненных запросов
    expect(rejectedRequests).toBe(0);
});

