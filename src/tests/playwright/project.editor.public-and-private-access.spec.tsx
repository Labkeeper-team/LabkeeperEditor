/*
Успешная компиляция публичного проекта незалогиненным пользователем
 */
test('public-project-unauth-user-compilation-ok', async ({ page }) => {
    let rejectedRequests = 0;

    // Блокируем все запросы по умолчанию
    await page.route('/api/*', async (route) => {
        rejectedRequests++;
        await route.abort();
    });

    // Разрешаем только необходимые запросы
    await page.route('/api/v2/public/user-info', async (route) => {
        await route.fulfill({
            status: 200,
            contentType: 'application/json',
            body: JSON.stringify({
                isAuthenticated: false,
                email: 'a@gmail.com',
                id: 1,
            }),
        });
    });

    await page.route('/api/v2/public/project/1/get', async (route) => {
        await route.fulfill({
            status: 200,
            contentType: 'application/json',
            body: JSON.stringify({
                projectId: 1,
                userId: 2,
                title: 'Default Project',
                lastModified: new Date().toISOString(),
                program: {
                    segments: [
                        {
                            id: 1,
                            type: 'md',
                            parameters: {
                                visible: true,
                            },
                            text: '# biba\n\n',
                        },
                    ],
                    parameters: {
                        roundStrategy: 'noRound',
                    },
                },
            }),
        });
    });

    await page.goto('/project/1');

    // Ждем загрузки страницы
    await page.waitForLoadState('networkidle');

    // Перехватываем запрос на компиляцию
    await page.route('/api/v2/public/project/1/compile', async (route) => {
        await route.fulfill({
            status: 200,
            contentType: 'application/json',
            body: JSON.stringify({
                segments: [
                    {
                        id: 1,
                        type: 'md',
                        statements: null,
                        text: '# biba\n\n',
                    },
                ],
            }),
        });
    });

    // компилируем
    await page.getByRole('button', { name: /Выполнить/i }).click();

    // ждем, когда кнопка снова станет нажимаемой
    await page
        .getByRole('button', { name: /Выполнить/i })
        .waitFor({ state: 'attached' });

    // проверяем, что элементы отображаются корректно
    await expect(page).toHaveScreenshot('public-compile-ok.png');

    // Проверяем, что не было отклоненных запросов
    expect(rejectedRequests).toBe(0);
});

