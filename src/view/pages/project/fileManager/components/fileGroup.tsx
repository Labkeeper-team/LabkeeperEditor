import { useSelector } from 'react-redux';
import { Typography } from '../../../../components/typography';
import { FileItem } from './fileItem';
import { useDictionary } from '../../../../../viewModel/store/selectors/translations';
import { StorageState } from '../../../../../viewModel/store';
import { LabkeeperFile } from '../../../../../model/domain.ts';

export const FileGroup = (props: {
    files: LabkeeperFile[];
    autogenerated: boolean;
}) => {
    const dictionary = useSelector(useDictionary);
    const isReadonly = useSelector(
        (state: StorageState) => state.project.projectIsReadonly
    );

    return (
        <div
            style={{
                display: 'flex',
                flexDirection: 'column',
                justifyContent: 'center',
                alignItems: 'flex-start',
                gap: 7,
                width: '100%',
            }}
        >
            <Typography
                text={
                    props.autogenerated
                        ? dictionary.filemanager.system_files
                        : dictionary.filemanager.your_files
                }
                type="label-small"
                color="#8C98B0"
            />
            {props.files.map((file) => {
                return (
                    <FileItem
                        showDropDown={!props.autogenerated && !isReadonly}
                        file={file}
                        key={file.fileName}
                    />
                );
            })}
        </div>
    );
};
